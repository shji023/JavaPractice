package Pack01;

public class DTO {
	String name;
	int age;
	String phone;
	String living_region;
	String role;
	String tool;
	String wts;
	DTO(){}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	} 
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getLiving_region() {
		return living_region;
	}
	public void setLiving_region(String living_region) {
		this.living_region = living_region;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getTool() {
		return tool;
	}
	public void setTool(String tool) {
		this.tool = tool;
	}
	public String getWts() {
		return wts;
	}
	public void setWts(String wts) {
		this.wts = wts;
	}
	
	
}
